package com.cordboard.week09;

public class DoWhile_077 {
    public static void main(String[] args) {
        /*
        note: prerequisite - while loop
        use a while loop to print numbers from 0 to 50 line by line
         */

        int num = 0;
        //Write Your code Here
        do{
            System.out.println(num);
            num++;
        }while(50 >= num);
    }
}
