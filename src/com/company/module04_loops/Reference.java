package com.company.module04_loops;

public class Reference {
    public static void main(String[] args) {
        Integer myInt=10;
        Double myDou=3.14;
        Character myChar='H';
        Boolean myBool=true;

        int getMyInt=myInt;
        double getMyDou=myDou;
        char getMyChar=myChar;
        boolean getMyBool=myBool;

        System.out.println(getMyInt );
        System.out.println(getMyDou );
        System.out.println(getMyChar );
        System.out.println(getMyBool );

    }
}
